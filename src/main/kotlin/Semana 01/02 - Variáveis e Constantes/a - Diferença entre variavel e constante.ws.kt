/*
a) Crie uma variável do tipo inteiro e atribua o valor 10. Em seguida, crie uma constante e
atribua o valor 5. Qual a diferença entre variáveis e constantes?

A diferença entre variáveis e constantes é que as variáveis podem ter seu valor alterado ao
longo do tempo, enquanto as constantes têm um valor imutável e não podem ser modificadas após
serem inicializadas.
*/

fun main() {

    var variavel: Int = 10
    val constante: Int = 5
    println("Valor da variável: $variavel")
    println("Valor da constante: $constante")
}